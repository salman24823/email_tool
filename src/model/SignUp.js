import mongoose from "mongoose";

const loginSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const loginModel =
  mongoose.models.Login || mongoose.model("Login", loginSchema);

export default loginModel;
